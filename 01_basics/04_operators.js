let x = 5;
let y = 3;

// Addition
console.log(`x + y = ${x + y}`); // 8

// Subtraction
console.log(`x - y = ${x - y}`); // 2

// Multiplication
console.log(`x * y = ${x * y}`); // 15

// Division
console.log(`x / y = ${x / y}`); // 1.6666666666667

// Reminder
console.log(`x % y = ${x % y}`); // 2

// Increament
console.log(`++x = ${ ++x }`); // 6
console.log(`x++ = ${ x++ }`); // 6
console.log(`x = ${ x }`); // 7

// Decreament
console.log(`--x = ${ --x }`); // 6
console.log(`x-- = ${ x-- }`); // 6
console.log(`x = ${ x }`); // 5

// Exponentiation
console.log(`x ** y = ${x ** y}`); // 125

// Comparison
console.log(x > y); // true

// Equal Operator
console.log((2 == 2)); // true

// Not Equal Operator
console.log((3 != 2)); // true

// Strict Equal Operator
console.log((2 === 2)); // true

// Strict Not Equal Operator
console.log((2 !== 2)); // false

// Logical Operator

// Logical AND
console.log((x < 6 && y < 5)); // true

// Logical OR
console.log((x < 6 || y > 6)); // true

// Logical NOT
console.log(!(x < 6)); // false